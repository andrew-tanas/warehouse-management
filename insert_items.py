def main():
    db_path = os.path.join(os.environ.get('USERPROFILE', ''), 'Documents', 'warehouse.db')
    if not os.path.exists(db_path):
        # We know from the previous script the DB is at .dart_tool
        db_path = r'D:\Projects\Warehouse Management\.dart_tool\sqflite_common_ffi\databases\warehouse.db'

    print(f"Connecting to {db_path}...")
    conn = sqlite3.connect(db_path)
    cursor = conn.cursor()

    count = 0
    for item_name, item_size, retail_price, wholesale_price in items:
        # Check if exists
        cursor.execute("SELECT id FROM items WHERE name = ? AND size = ?", (item_name, item_size))
        row = cursor.fetchone()
        if row:
            cursor.execute('''
                UPDATE items 
                SET retailPrice = ?, wholesalePrice = ?
                WHERE id = ?
            ''', (retail_price, wholesale_price, row[0]))
        else:
            cursor.execute('''
                INSERT INTO items (name, size, retailPrice, wholesalePrice, stockAmount)
                VALUES (?, ?, ?, ?, 0)
            ''', (item_name, item_size, retail_price, wholesale_price))
        count += 1

    conn.commit()
    conn.close()
    print(f"Successfully processed {count} items!")

if __name__ == '__main__':
    main()
